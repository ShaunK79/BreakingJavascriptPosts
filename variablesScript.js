// variables
// using a variable to store a number
let price;
let quantity;
let total;

price = 2.99;
quantity = 12;
total = price * quantity;
// executing to the webpage
document.write(`$${total}`); // $35.88

// using variable to store a string
let fname;
let message;
fname = 'Bob';
message = 'Good morning';
// execute to the webpage
document.write(`${message}, ${fname}!`); 
// Good morning, Bob!

// // using a variable to store a boolean
let inStock;
let shipping;
inStock = true;
shipping = false;

document.write(`We have 35 shirts in stock (${inStock}) and we can ship to Malta in 2 days (${shipping})`);

